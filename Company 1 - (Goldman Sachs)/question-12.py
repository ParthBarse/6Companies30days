# Question 12 - Puzzle | Program to find number of squares in a chessboard


# Solution -


def countSquares(n):
    return ((n * (n + 1) / 2) * (2 * n + 1) / 3)


if __name__ == "__main__":

    n = 4
    print("Count if Squares is ", countSquares(n))



